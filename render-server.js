app.post('/api/scrape', async (req, res) => {
    const { loginId, password, customerName, customerPhone } = req.body;

    if (!loginId || !password) {
        return res.status(400).json({
            success: false,
            error: 'Î°úÍ∑∏Ïù∏ Ï†ïÎ≥¥Í∞Ä ÌïÑÏöîÌï©ÎãàÎã§'
        });
    }

    try {
        const scraper = new ApiScraper();

        // Î°úÍ∑∏Ïù∏
        console.log(`Ïä§ÌÅ¨ÎûòÌïë ÏãúÏûë: ${customerName || loginId}`);
        const loginSuccess = await scraper.login(loginId, password);

        if (!loginSuccess) {
            return res.status(401).json({
                success: false,
                error: 'Î°úÍ∑∏Ïù∏Ïóê Ïã§Ìå®ÌñàÏäµÎãàÎã§'
            });
        }

        // Îç∞Ïù¥ÌÑ∞ Ï∂îÏ∂ú
        const data = await scraper.extractAllData();

        if (!data) {
            return res.status(500).json({
                success: false,
                error: 'Îç∞Ïù¥ÌÑ∞ Ï∂îÏ∂úÏóê Ïã§Ìå®ÌñàÏäµÎãàÎã§'
            });
        }

        // Í≥†Í∞ù Ï†ïÎ≥¥ Ï∂îÍ∞Ä
        if (customerName || customerPhone) {
            data.customerInfo = {
                name: customerName,
                phone: customerPhone
            };
        }

        // Notion Îç∞Ïù¥ÌÑ∞Î≤†Ïù¥Ïä§Ïóê Ï†ÄÏû• (ÏÑ§Ï†ïÎêòÏñ¥ ÏûàÎäî Í≤ΩÏö∞)
        let notionResult = null;
        if (process.env.NOTION_API_KEY && process.env.NOTION_DATABASE_ID) {
            try {
                const processor = new IntegratedDataProcessor();
                notionResult = await processor.processCompleteData({ data });
                console.log('‚úÖ Notion Ï†ÄÏû• ÏÑ±Í≥µ:', notionResult);
            } catch (notionError) {
                console.error('‚ùå Notion Ï†ÄÏû• Ïã§Ìå®:', notionError.message);
            }
        }

        // Make.com ÏõπÌõÖÏúºÎ°ú Ï†ÑÏÜ° (ÏÑ§Ï†ïÎêòÏñ¥ ÏûàÎäî Í≤ΩÏö∞)
        if (process.env.MAKE_WEBHOOK_URL) {
            try {
                const webhook = new MakeWebhookIntegration(process.env.MAKE_WEBHOOK_URL);
                await webhook.sendData(data, {
                    source: 'api-scraper',
                    timestamp: new Date().toISOString(),
                    notionResult: notionResult
                });
                console.log('ÏõπÌõÖ Ï†ÑÏÜ° ÏÑ±Í≥µ');
            } catch (webhookError) {
                console.error('ÏõπÌõÖ Ï†ÑÏÜ° Ïã§Ìå®:', webhookError.message);
            }
        }

        res.json({
            success: true,
            data: data,
            notionResult: notionResult
        });

    } catch (error) {
        console.error('Ïä§ÌÅ¨ÎûòÌïë Ïò§Î•ò:', error);
        res.status(500).json({
            success: false,
            error: error.message
        });
    }
});

